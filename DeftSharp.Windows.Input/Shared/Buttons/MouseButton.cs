namespace DeftSharp.Windows.Input.Mouse;

/// <summary>
/// Specifies the possible mouse buttons.
/// </summary>
public enum MouseButton : byte
{
    /// <summary>
    /// The left mouse button.
    /// </summary>
    Left = 0,

    /// <summary>
    /// The right mouse button.
    /// </summary>
    Right = 1,

    /// <summary>
    /// The middle mouse button.
    /// </summary>
    Middle = 2
}